package cn.cqupt.iprox.model.vo;

/**
 * 程涛
 * 根据（物种，组织，细胞类型，疾病类型）的查询条件
 * @author Cbillow
 *
 */
public class STTDType {

	private String species ;    //物种
	private String tissue ;		//组织
	private String cellType ;	//细胞类型
	private String disease ;	//疾病类型
	private Integer rows ;		//起始位置
	private Integer ProjectNum ;//查询总数
	private String type ;		//查询类型（物种，组织，细胞类型，疾病类型）
	
	public STTDType() {	}
	
	public Integer getRows() {
		return rows;
	}
	public void setRows(Integer rows) {
		this.rows = rows;
	}
	public Integer getProjectNum() {
		return ProjectNum;
	}
	public void setProjectNum(Integer projectNum) {
		ProjectNum = projectNum;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	public String getSpecies() {
		return species;
	}

	public void setSpecies(String species) {
		this.species = species;
	}

	public String getTissue() {
		return tissue;
	}

	public void setTissue(String tissue) {
		this.tissue = tissue;
	}

	public String getCellType() {
		return cellType;
	}

	public void setCellType(String cellType) {
		this.cellType = cellType;
	}

	public String getDisease() {
		return disease;
	}

	public void setDisease(String disease) {
		this.disease = disease;
	}
	
}
