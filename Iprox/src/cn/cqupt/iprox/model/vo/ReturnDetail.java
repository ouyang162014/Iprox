package cn.cqupt.iprox.model.vo;

import cn.cqupt.iprox.model.po.Group;
import cn.cqupt.iprox.model.po.Peptide;
import cn.cqupt.iprox.model.po.Project;
import cn.cqupt.iprox.model.po.Protein;
import cn.cqupt.iprox.model.po.Spectrum;
import cn.cqupt.iprox.model.po.SubProject;

/**
 * 返回有关联的页面详情
 * 包括：Group，Project，SubProject，Protein，Peptide，Spectrum
 * @author Cbillow
 *
 */
public class ReturnDetail {

	private Group group ;				//群组
	private Project project ;			//项目
	private SubProject subProject ;		//子项目
	private Protein protein ;			//蛋白质
	private Peptide peptide ;			//肽段
	private Spectrum spectrum ;			//谱图
	
	private Integer proteinCount;		//蛋白质数目
	private Integer peptideCount;		//肽段数目
	private Integer spectrumCount;		//谱图数目
	
	
	public Group getGroup() {
		return group;
	}
	public void setGroup(Group group) {
		this.group = group;
	}
	public Project getProject() {
		return project;
	}
	public void setProject(Project project) {
		this.project = project;
	}
	public SubProject getSubProject() {
		return subProject;
	}
	public void setSubProject(SubProject subProject) {
		this.subProject = subProject;
	}
	public Protein getProtein() {
		return protein;
	}
	public void setProtein(Protein protein) {
		this.protein = protein;
	}
	public Peptide getPeptide() {
		return peptide;
	}
	public void setPeptide(Peptide peptide) {
		this.peptide = peptide;
	}
	public Spectrum getSpectrum() {
		return spectrum;
	}
	public void setSpectrum(Spectrum spectrum) {
		this.spectrum = spectrum;
	}
	public Integer getProteinCount() {
		return proteinCount;
	}
	public void setProteinCount(Integer proteinCount) {
		this.proteinCount = proteinCount;
	}
	public Integer getPeptideCount() {
		return peptideCount;
	}
	public void setPeptideCount(Integer peptideCount) {
		this.peptideCount = peptideCount;
	}
	public Integer getSpectrumCount() {
		return spectrumCount;
	}
	public void setSpectrumCount(Integer spectrumCount) {
		this.spectrumCount = spectrumCount;
	}
	
}
